from gpiozero import LED, Button, LightSensor
from time import sleep

led1 = LED(17)
led2 = LED(27)
led3 = LED(22)
led4 = LED(23)
led5 = LED(24)
led6 = LED(25)
led7 = LED(12)

button = Button(26)
sensor = LightSensor(4, threshold=0.9)

sensor.wait_for_dark()
led1.on()
sleep(1)
sensor.wait_for_dark()
led2.on()
sleep(1)
sensor.wait_for_dark()
led3.on()
sleep(1)
sensor.wait_for_dark()
led4.on()
sleep(1)
sensor.wait_for_dark()
led5.on()
sleep(1)
sensor.wait_for_dark()
led6.on()
sleep(1)
sensor.wait_for_dark()
led7.on()
sleep(1)

button.wait_for_press()
led1.off()
led2.off()
led3.off()
led4.off()
led5.off()
led6.off()
led7.off()

